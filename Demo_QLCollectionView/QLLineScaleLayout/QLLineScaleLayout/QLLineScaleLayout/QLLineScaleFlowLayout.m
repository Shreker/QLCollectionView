/**
 *  ç”¨æ¥è®¾ç½®collectionViewåœæ­¢æ»šåŠ¨é‚£ä¸€åˆ»çš„ä½ç½®
 *
 *  @param proposedContentOffset åŸæœ¬collectionViewåœæ­¢æ»šåŠ¨é‚£ä¸€åˆ»çš„ä½ç½®
 *  @param velocity              æ»šåŠ¨é€Ÿåº¦
