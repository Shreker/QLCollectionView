#import "QLLineScaleFlowLayout.h"

@implementation QLLineScaleFlowLayout

static CGFloat const QLLineScaleFlowLayoutItemWH = 100;

- (instancetype)init {
    if (self = [super init]) {
        self.validDistance = 150;
        self.scaleFactor = 0.6;
    }
    return self;
}

/**
 *  ä¸€äº›åˆå§‹åŒ–å·¥ä½œæœ€å¥½åœ¨è¿™é‡Œå®ç°
